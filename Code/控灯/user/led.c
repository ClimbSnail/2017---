#include "led.h"

void led_init()
{
	RCC->APB2ENR|=1<<3;    //使能GPIOB	   	
	RCC->APB2ENR|=1<<2;    //使能GPIOA		
	RCC->APB2ENR|=1<<4;    //使能GPIOC
	   	 
	GPIOB->CRL&=0XFFFFFFF0; 
	GPIOB->CRL|=0X00000003;//PB.0推挽输出	 
	GPIOB->CRL&=0XFFFFFF0F; 
	GPIOB->CRL|=0X00000030;//PB.1推挽输出	
  GPIOA->CRL&=0X0FFFFFFF; 
	GPIOA->CRL|=0X30000000;//PA.7推挽输出	
	GPIOB->CRH&=0XFFF0FFFF; 
	GPIOB->CRH|=0X00030000;//PB.12推挽输出	
	GPIOC->CRH&=0XFF0FFFFF; 
	GPIOC->CRH|=0X00300000;//PC.13推挽输出

}

